          </p>
          <br>
          <h4>21. Restitución de equipos defectuosos, rescisión y devolución.Cuando el equipo presente defectos o no sirva para el uso que está destinado</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a optar por larestitución del equipo, rescindir el contrato y la devolución del precio pagado o pedir la reducción del precio. Además, encualquiera de los casos anteriores, 
            tienes derecho a la bonificación respectiva la cual no podrá ser menor del 20% del preciopagado.<sup>(47)</sup><br>
          </p>
