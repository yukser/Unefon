          <h4>6. Contrato registrado ante Profeco y el Instituto Federal de Telecomunicaciones.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a que el contrato de adhesión a través del cual contrates un servicio, esté autorizado y registradopor la Profeco y ante el Instituto Federal de Telecomunicaciones.<sup>(18)</sup><br>
          <br>
          El contrato de adhesión que te entregue el proveedor deberá también estar disponible en su portal de internet y deberácoincidir con el autorizado y registrado en Profeco y el Instituto Federal de Telecomunicaciones. 
