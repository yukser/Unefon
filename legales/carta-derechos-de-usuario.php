          <h4>17. Derecho a exigir calidad en el servicio.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a exigir la calidad que el proveedor se haya obligado a cumplir, a través de su publicidad o contratode adhesión. Recuerda que existen indicadores de 
            calidad para el servicio móvil, como intentos de llamadas fallidas, llamadasinterrumpidas, tiempo de establecimiento de llamadas, integridad de mensajes, sesiones de internet interrumpidas, 
            velocidadde datos promedio de descarga, entre otros, los cuales son revisados y publicados periódicamente por el Instituto Federal deTelecomunicaciones.<sup>(39)</sup>
          </br>
          TÚ TIENES DERECHO a que, en la prestación de los servicios, el proveedor cumpla con los estándares de calidaddeterminados por el Instituto Federal de Telecomunicaciones.<sup>(40)</sup></p>
