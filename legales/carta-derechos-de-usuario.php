            <br>
TÚ TIENES DERECHO al desbloqueo del equipo móvil cuando concluya el plazo del plan contratado, o bien, cuando hayasliquidado el precio total del equipo. Cuando lo hayas pagado de contado en 
un esquema de prepago, el proveedor o sudistribuidor te deberán proporcionar la clave de desbloqueo de tu equipo al momento de entregártelo.<sup>(12)</sup>
          </p>
          <br>

          <h3>III. DERECHO A CONDICIONES DE CONTRATACIÓN CLARAS, JUSTAS Y EQUITATIVAS.</h3>
          <h4>5. Información sobre términos y condiciones de contratación.</h4>
