          </p>
          <br>
          <h4>20. Reparaciones.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a que el tiempo que duren las reparaciones efectuadas a tu equipo por concepto de garantía, no sedescuente del plazo de garantía.<sup>(44)</sup>Cuando el equipo haya sido 
          reparado o repuesto, el proveedor deberá entregar una nueva garantía para las piezasrepuestas.<sup>(45)</sup>El proveedor debe asegurar que se te proporcione la infraestructura, capacidad técnica, mano de obra, 
