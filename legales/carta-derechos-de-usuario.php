          </p>
          <br>
          <h4>15. Consulta gratis tu saldo.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a consultar tu saldo sin costo y sin que te condicionen a comprar saldo adicional, así como al abonode tu saldo no consumido a la fecha de expiración, acumulándolo a 
