          <h4>18. Bonificación o descuento.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a que se te bonifique si el servicio no se presta conforme a la calidad y condiciones convenidas enel contrato, o por fallas o cargos indebidos por causas 
            imputables al proveedor, con al menos el 20% del monto del periodo deafectación y del precio del servicio. Lo anterior, sin que sea necesario que presentes una reclamación y sin perjuicio de ejercerlas 
            acciones legales respectivas para reclamar la indemnización que en su caso corresponda por daños y perjuicios.<sup>(41)</sup><br>
          </p>
          <br>
          <h4>19. Garantía mínima y póliza de garantía.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a que el proveedor te ofrezca una garantía del equipo, de cuando menos sesenta días naturales apartir de la entrega del mismo.<br>
          <br>
          Para tal efecto, el proveedor deberá entregarte con tu equipo, una póliza por escrito y en español que deberá contener demanera clara su alcance, duración, condiciones, mecanismos para hacerla efectiva, 
          domicilio para reclamaciones yestablecimiento o lugar para reparaciones.<sup>(42)</sup><br>
