            Dicho contrato deberá estar disponible en la página de Internet delproveedor.<sup>(13)</sup> Al momento de contratar, el proveedor deberá entregarte por escrito el contrato de adhesión, el cual debe 
            estar escrito enidioma español y con caracteres legibles a simple vista.<sup>(14)</sup> El contrato de adhesión deberá contener, cuando menos, la descripción del servicio que contratarás, las obligaciones 
            de laspartes, los términos y condiciones bajo los cuales se prestarán los servicios, las penas convencionales y los supuestos determinación y modificación. Asimismo, deberá contener las 
            especificaciones técnicas y comerciales, incluyendo la calidad y, ensu caso, velocidad del servicio de telecomunicaciones a contratar, entre otras.<sup>(15)</sup><br>
            <br>
            Asimismo, el contrato deberá indicar los medios disponibles de atención a clientes y, en su caso, horarios de atención delos mismos.<sup>(16)</sup> Tienes derecho a exigir al proveedor que cumpla con 
            las condiciones ofrecidas o implícitas en el contrato de adhesión y enla publicidad, así como en cualquier información y desplegados que difunda u ofrezca.<sup>(17)</sup>
          </p>
