          <p class="cuerpo-txt">TÚ TIENES DERECHO a que el contrato celebrado con el proveedor, únicamente pueda ser modificado con tuconsentimiento expreso.<sup>(20)</sup><br>
          <br>
          Tienes derecho a que cualquier modificación al contrato, esté previamente autorizada y registrada ante Profeco y el InstitutoFederal de Telecomunicaciones.<sup>(21)</sup><br>
          <br>
