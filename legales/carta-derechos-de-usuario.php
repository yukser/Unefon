          <br>
          <h4>16. Estados de cuenta, comprobantes de pago y/o facturas claras y desglosadas.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a que los estados de cuenta, comprobantes de pago y/o facturas que expida tu proveedor deservicios, sean claros, legibles y contengan de manera desglosada los 
            conceptos por el costo de los servicios, los equipos,servicios adicionales, cargos de instalación u otros accesorios como seguros, fianzas, membresías y demás cargos realizados.<sup>(36)</sup>En ningún caso, 
