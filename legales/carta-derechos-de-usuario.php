          <h3>VI. DERECHO A LA INFORMACIÓN.</h3>
          <h4>22. Información clara y oportuna.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a que el proveedor te informe antes de la contratación sobre los precios, tarifas, garantías,penalidades, compensaciones, cantidades, calidad, medidas, intereses, cargos, 
            términos, plazos, fechas, modalidades,reservaciones y demás condiciones de la prestación del servicio, conforme a las cuales se hubiera ofrecido, obligado oconvenido y, bajo ninguna circunstancia podrá negarte 
            dicho servicio.<sup>(48)</sup><br>
          </br>
          TÚ TIENES DERECHO a conocer la cobertura del proveedor, la cual podrás consultar en los centros de atención y/odistribución, en la página de internet del proveedor y deberá proporcionártela en caso 
          de que la solicites.<sup>(49)</sup></p>
          <br>
          <h4>23. Acceso a la información para la defensa de tus derechos.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a recibir una constancia de las reclamaciones, solicitudes y, en general, de todas las gestiones conincidencias en tu servicio que<br>
            realices ante el proveedor. Si el medio de atención del proveedor fue telefónico, el proveedorestá obligado a informarte que tienes derecho a recibir un documento que acredite la presentación y contenido de<br> 
            la reclamación, incidencia o gestión mediante cualquier soporte que permita tal acreditación.<sup>(50)</sup><br>
            <br>
            TÚ TIENES DERECHO a ser informado del estado de los trámites que has realizado ante el proveedor, en el momento en elque lo solicites.<br>
          </p>
          <br>
          <h4>24. Sistema de atención telefónica para quejas y consultas.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a que el proveedor te ofrezca una garantía del equipo, de cuando menos sesenta días naturales apartir de la entrega del mismo.</p>
          <br>
          TÚ TIENES DERECHO a acceder gratuitamente, las 24 horas del día, los trescientos sesenta y cinco días del año, a unsistema de atención telefónica o electrónico del proveedor, en el que podrás 
realizar cualquier tipo de consulta o presentar unareclamación sobre el servicio que se te proporciona.(51)En el caso de telefonía móvil, el sistema de atención deberá ser telefónico, para lo 
cual una vez que hayas accedido almismo y elijas hablar con un representante autorizado, el tiempo de espera para que te atiendan no deberá exceder de sesentasegundos.<sup>(52)</sup></p>
          <br>
          <h4>25. Tarifas registradas ante el Instituto Federal de Telecomunicaciones.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a que te respeten la tarifa registrada ante el Instituto Federal de Telecomunicaciones, que ofrecenlos proveedores de cualquier servicio de telecomunicaciones.<sup>(53)</sup><br>
          <br>
          En caso de que las tarifas que te apliquen no se encuentren registradas ante el Instituto, o si lo están, no se te respetan,puedes presentar tu queja o denuncia.
          </p>
          <br>
          <h3>VII. DERECHO A LA PRIVACIDAD Y A LA PROTECCIÓN DE DATOS PERSONALES.</h3>
          <h4>26. Protección de tus datos personales.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a que el proveedor resguarde y proteja tu información personal, como tu nombre, domicilio, correoelectrónico, número telefónico y otros.<sup>(54)</sup><br>
          <br>
          Asimismo, tiene la obligación de informarte qué datos recaba de ti y con qué fines los utilizará a través de un aviso deprivacidad. Tienes derecho a que los datos que proporcionaste sean usados únicamente 
          para los fines que autorizaste y entodo momento, tienes derecho a la seguridad del resguardo de los mismos.<sup>(55)</sup>En cualquier caso, puedes Acceder, Rectificar, Cancelar tu información y Oponerte a su uso, 
          lo cual se conoce comúnmentecomo "Derechos ARCO".<sup>(56)</sup><br>
          <br>
          En caso de pérdida o daño causado a tus datos personales, el proveedor debe notificarte.<sup>(57)</sup></p>
          <br>
          <h4>27. Protección de tus comunicaciones y los datos que las identifiquen.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a que el proveedor resguarde y proteja tus comunicaciones, así como los datos que identifiquen lasmismas, tales como fecha, hora y duración de las llamadas, 
            mensajes o datos que identifiquen el origen y destino de éstos,entre otros, garantizando su confidencialidad y privacidad.<sup>(58)</sup></p>
          <br>
          <h4>28. A no recibir llamadas o mensajes de promociones comerciales no autorizadas.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a no recibir llamadas de tu proveedor o de cualquier otro, para promover o vender servicios opaquetes, a menos que expresamente manifiestes tu consentimiento. <sup>(59)</sup></p>
          <br>
          <h3>VIII. DERECHOS DE ACCESIBILIDAD DE LOS USUARIOS CON DISCAPACIDAD.</h3>
          <h4>29. Equipos o dispositivos accesibles para personas con discapacidad.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a contar, previa solicitud de tu parte, con equipos o dispositivos, que tengan funcionalidades,programas o aplicaciones, 
            que permitan la accesibilidad a personas con discapacidad motriz, visual y auditiva.(60)Además, tienes derecho a acceder a un número telefónico para servicios de emergencia que contemple mecanismos que permitan 
            identificarte y ubicarte geográficamente.<sup>(61)</sup></p>
          <br>
          <h4>30. Las instalaciones o centros de atención contarán con adaptaciones y personal capacitado para atender apersonas con discapacidad.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a que las instalaciones o centros de atención al público de tu proveedor, cuenten con adaptaciones,modificaciones o mecanismos de accesibilidad, así como con personal 
capacitado para que tú recibas una atención adecuada.<sup>(62)</sup></p>
          <br>
          <h4>31. Páginas de Internet con funcionalidades de accesibilidad.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a que las páginas o portales de Internet, o números telefónicos de atención al público del proveedor,cuenten con funcionalidades de accesibilidad, 
y a recibir atención de su parte a través de personal capacitado.<sup>(63)</sup></p>
          <br>
          <h3>IX. DERECHO A LA NO DISCRIMINACIÓN.</h3>
          <h4>32. Derecho a no ser discriminado en el acceso a la contratación y prestación de los servicios de telecomunicaciones.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a acceder, contratar o que se te presten los servicios de telecomunicaciones, sin que el proveedor tepueda discriminar por origen étnico o nacionalidad, género, edad, discapacidades, 
condición social, salud, religión, opiniones,preferencias sexuales, estado civil o cualquier otro que menoscabe o anule tus derechos y libertades como persona.<sup>(64)</sup></p>
          <br>
          <h4>33. Derecho a la neutralidad de la red.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a acceder a cualquier contenido, aplicación o servicio ofrecido por el proveedor, dentro del marcolegal aplicable, sin limitaciones, degradaciones, restricciones o discriminaciones.<br>
          <br>
          TÚ TIENES DERECHO a conocer, a través del portal de internet del proveedor, las características del servicio, velocidad(como bits por segundo de subida y descarga de información, así como los factores 
          que pueden disminuir la velocidad delservicio) y calidad autorizados por el Instituto Federal de Telecomunicaciones.<sup>(65)</sup><br>
          <br>
          TÚ TIENES DERECHO a que el proveedor preserve tu privacidad y la seguridad de la red, a que tu proveedor no obstruya,interfiera, inspeccione, filtre o discrimine contenidos, aplicaciones o servicios. 
También tienes derecho a recibir la capacidad,velocidad y calidad que contrataste con independencia del contenido, origen, destino, aplicación o servicio que se te provean através de internet.<sup>(66)</sup></p>
          <br>
          <h3>X. DERECHO DE ACCESO GRATUITO A UN NÚMERO TELEFÓNICO DE SERVICIOS DE EMERGENCIA.</h3>
          <h4>34. Servicios de emergencia.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a que el proveedor te informe el número único de emergencias y a acceder de manera gratuita adicho número, el cual permitirá identificar y ubicar geográficamente tu llamada y, 
en su caso, los mensajes de texto que seenvíen.<sup>(67)</sup></p>
          <br>
           <h3>XI. DERECHO A LA SUSPENSIÓN DEL SERVICIO POR REPORTE DE ROBO O EXTRAVÍO</h3>
          <h4>35. Reporte de robo o extravío de equipos.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a reportar a tu operador a través de procedimientos expeditos, el robo o extravío de tu equipo móvil,para que éste suspenda inmediatamente el servicio de los equipos o dispositivos móviles.(68)<br>
            <br>
            Una vez que reportaste tu equipo como robado o extraviado, el proveedor no podrá seguirte cobrando el servicio.<sup>(69)</sup></p>
            <br>
           <h3>XII. DERECHOS ADICIONALES DE LOS USUARIOS DE TELEVISIÓN DE PAGA (POR CABLE O VÍA SATÉLITE).</h3>
          <h4>36. Recibir gratuitamente las señales de TV Abierta.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a recibir gratuitamente las señales de los canales de TV Abierta que se radiodifundan dentro de tu zona de cobertura geográfica y, 
            las señales de los canales transmitidos por Instituciones Públicas Federales, si cuentas con televisión de paga por cable.Si cuentas con televisión de paga por satélite, tienes derecho a recibir gratuitamente 
            las señales de TV Abierta de los canales "Canal de las Estrellas", "Canal 5", "Azteca Siete" y "Azteca Trece", así como, los canales transmitidos por InstitucionesPúblicas Federales.<sup>(70)</sup></p>
            <br>
            <h4>37. Bloqueo de aplicaciones o servicios.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a que los proveedores bloqueen los contenidos, aplicaciones o servicios que solicites. Podrásrealizar esta petición por los mismos medios disponibles 
            para realizar la contratación.<sup>(71)</sup></p>
            <br>
            <h4>38. Control parental.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a que los proveedores te proporcionen un servicio de control parental previa solicitud de tu parte, yque publiquen de manera clara las características operativas de este servicio y 
            las instrucciones para que puedas operar lasaplicaciones necesarias para su correcto funcionamiento.<sup>(72)</sup></p>
            <br>
            <h3>XIII. DERECHO A LA PROTECCIÓN.</h3>
          <h4>39. Derecho al acceso a los órganos administrativos.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a la protección y representación de Profeco para asegurar que tus derechos sean respetados, convistas a la 
            prevención de daños patrimoniales y morales individuales y colectivos, garantizando la protección jurídica,económica administrativa y técnica.<sup>(73)</sup><br>
          </p>
          <br>
           <h4>40. Interposición de quejas y denuncias por incumplimientos del proveedor.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a la protección y representación de Profeco para asegurar que tus derechos sean respetados, convistas a la 
            prevención de daños patrimoniales y morales individuales y colectivos, garantizando la protección jurídica,económica administrativa y técnica.<sup>(73)</sup><br>
          </p>
          <br>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a presentar quejas y denuncias ante el Instituto Federal de Telecomunicaciones y la ProcuraduríaFederal del Consumidor, ante cualquier incumplimiento del proveedor.
            Al iniciar un procedimiento conciliatorio derivado de una queja ante Profeco, tienes derecho a que, mientras dure el mismo,no te suspendan los servicios de telecomunicaciones por falta de pago.<sup>(74)</sup><br>
            <br>
            Tienes derecho a presentar una queja ante el Instituto Federal de Telecomunicaciones por las fallas de los servicios oincumplimientos de los operadores a sus obligaciones, a efecto de que se hagan 
            las verificaciones correspondientes y, en sucaso, se inicie un procedimiento de sanción.</p>
            <br>
            <h4>41. Representación colectiva.</h4>
            <p class="cuerpo-txt">TÚ TIENES DERECHO a ser representado en una acción colectiva ante el incumplimiento de un proveedor, para buscarque se declare que el proveedor incurrió en conductas que dañaron 
              tus intereses y tu patrimonio y que lo condenen a laindemnización de daños y perjuicios. El representante de tal colectividad o grupo puede ser Profeco, una asociación deconsumidores registrada o bien 
              un grupo de por lo menos 30 consumidores afectados que firmen la demanda.<sup>(75)</sup>Estos son tus principales derechos como usuario de servicios de telecomunicaciones, existen otros que se encuentran 
              en las leyes, reglamentos y disposiciones administrativas vigentes y aplicables que el proveedor de servicios tiene la obligación derespetar.Si quieres conocer más sobre tus derechos, visita la página de Internet 
              del Instituto Federal de Telecomunicacioneswww.ift.org.mx o la página de la Procuraduría Federal del Consumidor <a href="http://www.profeco.gob.mx" target="_blank" class="blue">www.profeco.gob.mx</a> y recuerda 
              TÚ TIENES DERECHO."</p>
