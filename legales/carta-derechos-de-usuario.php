          <h4>3. Portabilidad.</h4>
          <p class="cuerpo-txt">TÚ TIENES DERECHO a cambiarte gratuitamente de compañía conservando tu mismo número telefónico, tantas vecescomo lo decidas, 
            acudiendo con la nueva compañía con la que deseas contratar, quien deberá realizar la portabilidad durantelas 24 horas siguientes a tu solicitud, o si así lo deseas, 
            tienes derecho a decidir sobre la fecha en que quieres que se ejecute laportabilidad, siempre y cuando ésta no exceda de 5 días hábiles a partir de que presentes tu solicitud.<sup>(6)</sup></br>
            <br>
            TÚ TIENES DERECHO a partir de la fecha en que se ejecute la portabilidad y sin la exigencia de requisitos adicionales, afiniquitar de manera automática la relación contractual con el proveedor 
que antes te prestaba el servicio, únicamente deaquellos servicios cuya prestación requiera de los números telefónicos a ser portados, por lo que los servicios que no requierande los números 
telefónicos portados podrán continuar activos en los términos establecidos en los contratos.<sup>(7)</sup><br>
             <br>
             TÚ TIENES DERECHO a que el proveedor con el que decidas cambiarte, te proporcione la información necesaria paraacceder al Sistema de Información en internet que te permita saber el estado que 
guarda tu solicitud de portabilidad.<sup>(8)</sup> Una vez que has iniciado el proceso de portabilidad, si así lo decides, tienes derecho a cancelarlo de manera gratuita.<sup>(9)</sup><br>
<br>
TÚ TIENES DERECHO a recibir el pago de las penas convencionales previstas en el contrato, en caso de que no secumplan los plazos de portabilidad correspondientes.<sup>(10)</sup><br>
<br>
TÚ TIENES DERECHO a realizar la portabilidad de tu número telefónico pese a tener adeudos pendientes con el proveedorque te prestaba los servicios de telecomunicaciones, sin que ello 
te exente de pagar losadeudos pendientes y, en su caso, las penalizaciones por terminación anticipada, así como devolver los equipos que no seande tu propiedad.<sup>(11)</sup><br>
          </p>
